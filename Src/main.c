/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	SCB->VTOR = FLASH_BASE | 0x10000;
	__set_FAULTMASK(0);

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
 // MX_LWIP_Init();
  MX_USART1_UART_Init();
  MX_USART6_UART_Init();
  MX_SPI3_Init();
  MX_SPI2_Init();
  MX_USART3_UART_Init();
 // MX_WWDG_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
	PLATFORM_Init();
	MX_LWIP_Init();

	static struct tftp_context tftp = {tftp_open, tftp_close, tftp_read, tftp_write};
	tftp_init(&tftp);
	//static RTC_TimeTypeDef stimestructureget;
	//static RTC_DateTypeDef sdatestructureget;
	static uint32_t Tick = 0;
	Tick = HAL_GetTick();

	if(eMBTCPInit(502) != MB_ENOERR)
		Soft_Reset();
	eMBEnable();
	MX_WWDG_Init();
  /* Infinite loop */
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		//tftp 增加closehandle();
		WWDG_Refresh();
		eMBPoll();
		if(HAL_GetTick() - Tick >= 1000) {
			HAL_GPIO_TogglePin(APP_RUN_GPIO_Port, APP_RUN_Pin);
			Tick = HAL_GetTick();

			//检测内存
			struct tcp_pcb *pcb = (struct tcp_pcb *)memp_malloc(MEMP_TCP_PCB);
			if(pcb == NULL)
				Soft_Reset();
			memp_free(MEMP_TCP_PCB, pcb);

			struct tcp_seg *seg = (struct tcp_seg *)memp_malloc(MEMP_TCP_SEG);
			if(seg == NULL)
				Soft_Reset();
			memp_free(MEMP_TCP_SEG, seg);

			struct pbuf *buf = pbuf_alloc(PBUF_TRANSPORT, 1600, PBUF_RAM);
		  if(buf == NULL)
				Soft_Reset();
			pbuf_free(buf);
		}
		MX_LWIP_Process();
  }
  /* USER CODE END 3 */

}

